// Color utility functions
export const colorUtils = {
  /**
   * Get a color with opacity
   * @param {string} color - The color value
   * @param {number} opacity - The opacity value (0-1)
   * @returns {string} - Color with opacity
   */
  withOpacity: (color, opacity) => {
    // Convert hex to rgba if needed
    if (color.startsWith('#')) {
      const hex = color.replace('#', '');
      const r = parseInt(hex.substr(0, 2), 16);
      const g = parseInt(hex.substr(2, 2), 16);
      const b = parseInt(hex.substr(4, 2), 16);
      return `rgba(${r}, ${g}, ${b}, ${opacity})`;
    }
    return color;
  },

  /**
   * Get a random color from a specific category
   * @param {string} category - The color category
   * @returns {string} - Random color from the category
   */
  getRandomColor: (category = 'primary') => {
    const categoryColors = colors[category];
    if (!categoryColors) return colors.primary[500];
    
    const shades = Object.keys(categoryColors);
    const randomShade = shades[Math.floor(Math.random() * shades.length)];
    return categoryColors[randomShade];
  },

  /**
   * Get all shades of a color category
   * @param {string} category - The color category
   * @returns {object} - All shades of the category
   */
  getColorShades: (category) => {
    return colors[category] || {};
  },
};

